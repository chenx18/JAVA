/**
 * 我的第一个Java程序
 * 这是一个经典的Hello World示例
 */
public class HelloWorld {

    // 程序的入口点 - main方法
    public static void main(String[] args) {

        // 在控制台输出文本
        System.out.println("Hello, World!");
        System.out.println("欢迎学习Java！");

    }
}


// // 笔记：
// // 1. java程序的入口是 main 方法
// // 2. 使用 System.out.println() 输出内容
// // 3. .java用于写源码，.class 是javac编译后运行的字节码文件
// // 4. 编译命令： javac -encoding UTF-8 Hellowrld.java
// // 5. 运行命令：java HelloWorld
// // 6. 文件命名规则：文件名必须与 public calss 的类名一致

// // java程序基本结构
// public calss 类名 {
//     public static void main(String[] args) {
//         // 代码
//     }
// }

// // 输出语句
// System.out.println() - 输出后换行
// System.out.print() - 输出不换行

// // 编译与运行
// 编译： javac -encoding UTF-8 文件名.java
// 运行： java 类名 （注意 不带.class）

// // 文件命名规则
// 文件名必须与 public class 的类名一致
// 区分大小写

// // .java vs .class
// .java = 源代码
// .calss = 字节码